package com.guigu.salmanager.model;

public class SaleOrder {
	
	private String sale_id;				//订单id
	private String bill_no;			//订单号
	private String salesman_id;		//经手人id
	private String category_id;		// '所属分类id'
	private String warehouse_id;	//'所属仓库id'
	private String amount; 			// '销售数量'
	private String goods_id;		//--'商品id'
	private String customer_id;
	private int del_flag; 		//'删除标识{1表示已删除，0表示未删除}'
	public SaleOrder() {
		super();
	}
	
	public SaleOrder(String sale_id, String bill_no, String salesman_id, String category_id, String warehouse_id,
			String amount, String goods_id, String customer_id, int del_flag) {
		super();
		this.sale_id = sale_id;
		this.bill_no = bill_no;
		this.salesman_id = salesman_id;
		this.category_id = category_id;
		this.warehouse_id = warehouse_id;
		this.amount = amount;
		this.goods_id = goods_id;
		this.customer_id = customer_id;
		this.del_flag = del_flag;
	}

	public String getSale_id() {
		return sale_id;
	}
	public void setSale_id(String id) {
		this.sale_id = id;
	}
	public String getBill_no() {
		return bill_no;
	}
	public void setBill_no(String bill_no) {
		this.bill_no = bill_no;
	}
	public String getSalesman_id() {
		return salesman_id;
	}
	public void setSalesman_id(String salesman_id) {
		this.salesman_id = salesman_id;
	}
	public String getCategory_id() {
		return category_id;
	}
	public void setCategory_id(String category_id) {
		this.category_id = category_id;
	}
	public String getWarehouse_id() {
		return warehouse_id;
	}
	public void setWarehouse_id(String warehouse_id) {
		this.warehouse_id = warehouse_id;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getGoods_id() {
		return goods_id;
	}
	public void setGoods_id(String goods_id) {
		this.goods_id = goods_id;
	}
	public int getDel_flag() {
		return del_flag;
	}
	public void setDel_flag(int del_flag) {
		this.del_flag = del_flag;
	}
	
	public String getCustomer_id() {
		return customer_id;
	}
	public void setCustomer_id(String customer_id) {
		this.customer_id = customer_id;
	}
	@Override
	public String toString() {
		return "SaleOrder [sale_id=" + sale_id + ", bill_no=" + bill_no + ", salesman_id=" + salesman_id
				+ ", category_id=" + category_id + ", warehouse_id=" + warehouse_id + ", amount=" + amount
				+ ", goods_id=" + goods_id + ", customer_id=" + customer_id + ", del_flag=" + del_flag + "]";
	}

}
