package com.guigu.salmanager.model;

public class Goods {
	private String goods_Id;

	private String name;

	private double price;

	private String origin;

	private double stock;

	private String warehouse_Id;

	private String category_Id;

	private int delFlag;

	public Goods() {
		super();
	}

	public Goods(String goods_Id, String name, double price, String origin, double stock, String warehouse_Id,
			String category_Id, int delFlag) {
		super();
		this.goods_Id = goods_Id;
		this.name = name;
		this.price = price;
		this.origin = origin;
		this.stock = stock;
		this.warehouse_Id = warehouse_Id;
		this.category_Id = category_Id;
		this.delFlag = delFlag;
	}

	public String getGoods_Id() {
		return goods_Id;
	}

	public void setGoods_Id(String goods_Id) {
		this.goods_Id = goods_Id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getPrice() {
		return price;
	}

	public void setPrice(double price) {
		this.price = price;
	}

	public String getOrigin() {
		return origin;
	}

	public void setOrigin(String origin) {
		this.origin = origin;
	}

	public double getStock() {
		return stock;
	}

	public void setStock(double stock) {
		this.stock = stock;
	}

	public String getWarehouse_Id() {
		return warehouse_Id;
	}

	public void setWarehouse_Id(String warehouse_Id) {
		this.warehouse_Id = warehouse_Id;
	}

	public String getCategory_Id() {
		return category_Id;
	}

	public void setCategory_Id(String category_Id) {
		this.category_Id = category_Id;
	}

	public int getDelFlag() {
		return delFlag;
	}

	public void setDelFlag(int delFlag) {
		this.delFlag = delFlag;
	}

	@Override
	public String toString() {
		return "Goods [goods_Id=" + goods_Id + ", name=" + name + ", price=" + price + ", origin=" + origin + ", stock="
				+ stock + ", warehouse_Id=" + warehouse_Id + ", category_Id=" + category_Id + ", delFlag=" + delFlag
				+ "]";
	}
	
	

}
